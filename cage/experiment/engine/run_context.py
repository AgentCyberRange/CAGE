"""The live state of one experiment invocation.

``ExperimentRun`` is the single object an experiment runs from. It carries the
resolved configuration ``project.yml`` deserialized into (benchmark, agents,
models, and the parsed :mod:`cage.config.sections` value objects) *and* the
per-invocation collaborators the conductor binds at run time (the run id, the
run-output root, the scheduler and cleanup, and — once built — the progress
reporter).

Holding both on one object is deliberate: a run *is* its configuration plus its
live machinery. Passing it explicitly (rather than via module globals) keeps the
conductor reentrant — two runs in one process can no longer clobber each other's
scheduler or cleanup state.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from pathlib import Path
from typing import TYPE_CHECKING, Any

from cage.config.sections import (
    ExecutionConfig,
    JudgeConfig,
    OutputConfig,
    ProxyConfig,
    ResumeKeepIf,
    TargetConfig,
)
from cage.contracts.logging import LoggingConfig
from cage.experiment.engine.hooks import HookRegistry
from cage.experiment.engine.run_cleanup import RunCleanup
from cage.experiment.engine.scheduler import RunScheduler
from cage.sandbox.admission import AdmissionConfig

if TYPE_CHECKING:
    from cage.agents.base import AgentInstance
    from cage.benchmarks import Benchmark
    from cage.experiment.model import ExperimentSpec
    from cage.models import ModelConfig


@dataclass
class ExperimentRun:
    """The resolved experiment plus the live context one invocation runs from."""

    # --- identity & resolved configuration (built by ``resolve``) ---
    name: str = ""
    project_file: Path = field(default_factory=lambda: Path("project.yml"))
    benchmark: "Benchmark | None" = None
    agents: "list[AgentInstance]" = field(default_factory=list)
    models: "dict[str, ModelConfig]" = field(default_factory=dict)
    benchmark_dir: Path | None = None
    # The pure, declarative view of the same project mapping this run was
    # resolved from (one parser: ``experiment_spec_from_project_mapping``).
    # Canonical artifact snapshots derive from it instead of re-projecting
    # resolved runtime objects, so the recorded spec cannot drift from the
    # parse that actually drove the run.
    spec: "ExperimentSpec | None" = None
    # User-specified sample selection (Layer-3 run knobs: ``eval.limit`` /
    # ``--limit`` and ``--sample``). Carried on the run, not smuggled onto the
    # benchmark instance, and passed explicitly to ``iter_samples_limited``.
    sample_limit: int | None = None
    sample_ids: tuple[str, ...] = ()
    # Python-style window over the ordered, id-filtered sample list
    # (``eval.sample_slice`` / ``--sample-slice``). Applied in
    # ``iter_samples_limited`` after ``sample_ids`` and before ``sample_limit``.
    sample_slice: slice | None = None
    judge: JudgeConfig | None = None
    hooks: HookRegistry = field(default_factory=HookRegistry)
    proxy: ProxyConfig = field(default_factory=ProxyConfig)
    execution: ExecutionConfig = field(default_factory=ExecutionConfig)
    logging: LoggingConfig = field(default_factory=LoggingConfig)
    target: TargetConfig = field(default_factory=TargetConfig)
    output: OutputConfig = field(default_factory=OutputConfig)
    admission: AdmissionConfig = field(default_factory=AdmissionConfig)
    metadata: dict[str, Any] = field(default_factory=dict)
    # User-specified run id (optional). Auto-generated by the conductor when empty.
    run_id: str = ""
    # Archive any existing run directories with the same run_id before starting
    # a fresh run. Mutually exclusive with ``resume``.
    force: bool = False
    # Resume an existing run: don't error when the run_dir already exists.
    # Default policy re-runs only trials whose previous attempt didn't produce
    # a valid result; other completed-with-failure outcomes replay from disk
    # unless their ``termination_reason`` is listed in ``resume_retry_reasons``.
    # See ``cage/experiment/engine/resume.py``.
    resume: bool = False
    resume_retry_reasons: list[str] = field(default_factory=list)
    # Bounds total tries per trial (``len(archives) + 1``). 0 = unlimited.
    resume_max_attempts: int = 0
    # ``resume.select.id_matches`` — positive gate: when set, ONLY trials whose
    # ``trial_id`` matches this regex are eligible to be re-run.
    resume_select_id_pattern: str | None = None
    # ``resume.keep_if`` — veto thresholds; see :class:`ResumeKeepIf`.
    resume_keep_if: ResumeKeepIf = field(default_factory=ResumeKeepIf)

    # --- live run context (bound by the conductor at run time) ---
    cage_runs: Path | None = None
    scheduler: RunScheduler = field(default_factory=RunScheduler.inactive)
    cleanup: RunCleanup = field(default_factory=RunCleanup.inactive)
    reporter: Any | None = None

    @classmethod
    def inactive(cls) -> "ExperimentRun":
        """A no-op run context (inactive scheduler/cleanup, no resolved config).

        For collaborators exercised outside a live ``run_experiment`` (e.g. a unit
        test calling a trial helper directly) that read only the scheduler/cleanup.
        """
        return cls()
